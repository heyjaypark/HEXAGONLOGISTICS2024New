package product.model;



public class ProductSample {
	
	private int p_no;
	private String p_name;

	
	

	public ProductSample(int p_no, String p_name) {
		this.p_no = p_no;
		this.p_name = p_name;

	}


	public int getP_no() {
		return p_no;
	}


	public void setP_no(int p_no) {
		this.p_no = p_no;
	}


	public String getP_name() {
		return p_name;
	}


	public void setP_name(String p_name) {
		this.p_name = p_name;
	}



}
